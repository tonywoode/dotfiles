  panel = {
    auto_refresh = true, --in the panel, prev and next are [[,] and accept is <CR>
  },
  suggestion = {
    auto_trigger = true, --in the main ide, prev and next are opt+[,opt+] and accept is opt+l, dismiss is ctrl+]
  }
})

